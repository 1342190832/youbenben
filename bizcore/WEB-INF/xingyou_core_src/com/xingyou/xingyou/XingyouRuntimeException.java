
package com.xingyou.xingyou;


public class XingyouRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public XingyouRuntimeException() {
        super();
    }


    public XingyouRuntimeException(String message) {
        super(message);
    }


    public XingyouRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public XingyouRuntimeException(Throwable cause) {
        super(cause);
    }


   
}


















