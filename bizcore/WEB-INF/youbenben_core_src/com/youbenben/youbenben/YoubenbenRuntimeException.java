
package com.youbenben.youbenben;


public class YoubenbenRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public YoubenbenRuntimeException() {
        super();
    }


    public YoubenbenRuntimeException(String message) {
        super(message);
    }


    public YoubenbenRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public YoubenbenRuntimeException(Throwable cause) {
        super(cause);
    }


   
}


















